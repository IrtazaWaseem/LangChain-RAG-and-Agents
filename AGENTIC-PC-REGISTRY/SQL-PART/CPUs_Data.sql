DROP TABLE CPUS CASCADE CONSTRAINTS;
CREATE TABLE CPUS (
    CPU_ID NUMBER PRIMARY KEY,
    Brand VARCHAR2(50) NOT NULL,
    Model VARCHAR2(100) NOT NULL UNIQUE,
    Generation VARCHAR2(50),
    Core_Count NUMBER,
    Thread_Count NUMBER,
    Base_Clock_GHz NUMBER(4,2),
    Boost_Clock_GHz NUMBER(4,2),
    TDP_W NUMBER,
    Benchmark_Score NUMBER NOT NULL,
    Release_Year NUMBER(4),
    Tier_ID NUMBER,
    CONSTRAINT chk_cpu_brand CHECK (Brand IN ('Intel', 'AMD', 'Apple')),
    CONSTRAINT chk_cores CHECK (Core_Count > 0),
    CONSTRAINT fk_cpu_tier FOREIGN KEY (Tier_ID) REFERENCES HARDWARE_TIERS(Tier_ID)
);

