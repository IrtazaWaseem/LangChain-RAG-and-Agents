DROP TABLE GPUS CASCADE CONSTRAINTS;

CREATE TABLE GPUS (
    GPU_ID NUMBER PRIMARY KEY,
    Brand VARCHAR2(50) NOT NULL,
    Model VARCHAR2(100) NOT NULL UNIQUE,
    VRAM_GB NUMBER,
    Memory_Type VARCHAR2(50),
    Power_W NUMBER,
    Benchmark_Score NUMBER NOT NULL,
    Release_Year NUMBER(4),
    Tier_ID NUMBER,
    CONSTRAINT chk_gpu_brand CHECK (Brand IN ('NVIDIA', 'AMD', 'Intel')),
    CONSTRAINT chk_vram CHECK (VRAM_GB > 0),
    CONSTRAINT fk_gpu_tier FOREIGN KEY (Tier_ID) REFERENCES HARDWARE_TIERS(Tier_ID)
);

