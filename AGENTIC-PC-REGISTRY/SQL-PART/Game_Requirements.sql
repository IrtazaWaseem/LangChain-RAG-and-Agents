CREATE TABLE GAME_REQUIREMENTS (
    Req_ID NUMBER PRIMARY KEY,
    Game_ID NUMBER NOT NULL,
    Req_Type VARCHAR2(3) NOT NULL,
    OS_ID NUMBER NOT NULL,
    CPU_ID NUMBER NOT NULL,
    GPU_ID NUMBER NOT NULL,
    RAM_ID NUMBER NOT NULL,
    CONSTRAINT chk_req_type CHECK (Req_Type IN ('MIN', 'REC')),
    CONSTRAINT uq_game_req UNIQUE (Game_ID, Req_Type),
    CONSTRAINT fk_req_game FOREIGN KEY (Game_ID) REFERENCES GAMES(Game_ID),
    CONSTRAINT fk_req_os FOREIGN KEY (OS_ID) REFERENCES OPERATING_SYSTEMS(OS_ID),
    CONSTRAINT fk_req_cpu FOREIGN KEY (CPU_ID) REFERENCES CPUS(CPU_ID),
    CONSTRAINT fk_req_gpu FOREIGN KEY (GPU_ID) REFERENCES GPUS(GPU_ID),
    CONSTRAINT fk_req_ram FOREIGN KEY (RAM_ID) REFERENCES RAM_CONFIGURATIONS(RAM_ID)
);

