DROP TABLE SSDS CASCADE CONSTRAINTS;
CREATE TABLE SSDS (
    SSD_ID NUMBER PRIMARY KEY,
    Brand VARCHAR2(50) NOT NULL,
    Model VARCHAR2(100) NOT NULL, 
    Capacity_GB NUMBER NOT NULL,
    Form_Factor VARCHAR2(50),
    Read_Speed_MBps NUMBER,
    Write_Speed_MBps NUMBER,
    Tier_ID NUMBER,
    CONSTRAINT chk_ssd_capacity CHECK (Capacity_GB > 0),
    CONSTRAINT chk_ssd_speeds CHECK (Read_Speed_MBps > 0 AND Write_Speed_MBps > 0),
    CONSTRAINT fk_ssd_tier FOREIGN KEY (Tier_ID) REFERENCES HARDWARE_TIERS(Tier_ID)
);

